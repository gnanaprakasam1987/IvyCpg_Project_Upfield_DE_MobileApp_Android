package com.ivy.cpg.view.order.scheme;

public class SchemeProductBatchQty {

	private int qty;
	private int caseQty;
	private int outerQty;
	private int batchid;

	public int getQty() {
		return qty;
	}

	public void setQty(int qty) {
		this.qty = qty;
	}

	public int getBatchid() {
		return batchid;
	}

	public void setBatchid(int batchid) {
		this.batchid = batchid;
	}
	public int getCaseQty() {
		return caseQty;
	}

	public void setCaseQty(int caseQty) {
		this.caseQty = caseQty;
	}

	public int getOuterQty() {
		return outerQty;
	}

	public void setOuterQty(int outerQty) {
		this.outerQty = outerQty;
	}

}
