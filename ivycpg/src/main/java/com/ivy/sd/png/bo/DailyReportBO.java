package com.ivy.sd.png.bo;

public class DailyReportBO {

	private String effCoverage;
	private String totLines;
	private String totValues;
	private String avgLines;
	private String mspValues;

	public String getEffCoverage() {
		return effCoverage;
	}

	public void setEffCoverage(String effCoverage) {
		this.effCoverage = effCoverage;
	}

	public String getTotLines() {
		return totLines;
	}

	public void setTotLines(String totLines) {
		this.totLines = totLines;
	}

	public String getTotValues() {
		return totValues;
	}

	public void setTotValues(String totValues) {
		this.totValues = totValues;
	}

	public String getAvgLines() {
		return avgLines;
	}

	public void setAvgLines(String avgLines) {
		this.avgLines = avgLines;
	}

	public String getMspValues() {
		return mspValues;
	}

	public void setMspValues(String mspValues) {
		this.mspValues = mspValues;
	}
}
